           hi: "India",
           vi: "Vietnam",
           th: "Thailand",
           id: "Indonesia",
           tr: "Turkey",
           pl: "Poland",
           uk: "Ukraine",
           nl: "Netherlands",
           sv: "Sweden",
           da: "Denmark",
           fi: "Finland",
           no: "Norway",
           cs: "Czech Republic",
           ro: "Romania",
           hu: "Hungary",
           el: "Greece",
           he: "Israel",
           fa: "Iran",
           ms: "Malaysia",
           bn: "Bangladesh",
           ur: "Pakistan",
           ta: "India",
           te: "India",
           mr: "India",
           pa: "India",
           ml: "India",
           kn: "India",
           or: "India",
           gu: "India",
           si: "Sri Lanka",
           my: "Myanmar",
           ne: "Nepal",
           km: "Cambodia",
           lo: "Laos",
           am: "Ethiopia",
