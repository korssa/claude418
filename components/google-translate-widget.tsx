           sq: "Albania",
           az: "Azerbaijan",
           hy: "Armenia",
           mk: "North Macedonia",
           sr: "Serbia",
           ka: "Georgia",
           lt: "Lithuania",
           lv: "Latvia",
           et: "Estonia",
           sk: "Slovakia",
